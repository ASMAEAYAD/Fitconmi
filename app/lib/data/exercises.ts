// ── Lookup helpers ────────────────────────────────────────────────

export const exerciseById = (id: string): Exercise | undefined =>
  exercises.find((e) => e.id === id);

export const exerciseBySlug = (slug: string): Exercise | undefined =>
  exercises.find((e) => e.slug === slug);

export const exercisesByMuscle = (primary: string): Exercise[] =>
  exercises.filter((e) => e.muscleGroup.primary === primary);

export const exercisesByEquipment = (eq: string): Exercise[] =>
  exercises.filter((e) => e.equipment.includes(eq as Exercise["equipment"][number]));

export const exercisesByTag = (tag: string): Exercise[] =>
  exercises.filter((e) => e.tags.includes(tag));
